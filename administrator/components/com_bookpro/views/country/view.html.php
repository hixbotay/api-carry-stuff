defined('_JEXEC') or die;

class BookproViewCountry extends JViewLegacy
{
	protected $form;
	protected $item;
	protected $state;

	/**
	 * (non-PHPdoc)
	 * @see JViewLegacy::display()
	 */
	public function display($tpl = null)
	{
		$this->form		= $this->get('Form');
		$this->item		= $this->get('Item');
		$this->state	= $this->get('State');
		$this->addToolbar();
		parent::display($tpl);
	}

	/**
	 * Add the page title and toolbar.
	 *
	 * @since	1.6
	 */
	protected function addToolbar()
	{
		JRequest::setVar('hidemainmenu', true);
		JToolBarHelper::title(JText::_('COM_BOOKPRO_COUNTRY_EDIT'), 'country');
		JToolBarHelper::apply('country.apply');
		JToolBarHelper::save('country.save');
		JToolBarHelper::cancel('country.cancel');
	}
}